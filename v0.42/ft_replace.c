#include "libfil.h"
#include "libft.h"

/*
** =======================================
**				ft_replace.
** COMPLETE "tab" WITH (SIZE * SIZE) '.'
** =======================================
*/

void	ft_replace(int size, char (*tab)[size])
{
	t_point	co;

	co.x = -1;
	while (++co.x < size)
	{
		co.y = -1;
		while (++co.y < size)
			tab[co.x][co.y] = '.';
	}
}
