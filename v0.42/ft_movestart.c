#include "libft.h"
#include "libfil.h"

/*
** =======================================
**           ft_movestart.c
**      MOVE *buf WHILE NECESSARY
** =======================================
*/

int	ft_movestart(char **buf)
{
	int i;

	i = 0;
	while (*(*buf + 5) != '#' && **buf != '#')
		++(*buf);
	if (*(*buf + 5) == '#' && **buf != '#')
	{
		while ((*buf)[i] != '#' && (*buf)[i] != '\n')
			++i;
		if ((*buf)[i] == '\n')
		{
			*buf += i + 1;
			return (ft_movestart(&(*buf)));
		}
	}
	return (0);
}
