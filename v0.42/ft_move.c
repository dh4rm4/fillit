#include "libft.h"
#include "libfil.h"

/*
** =======================================
**              ft_move.c
** 		MOVE *buf WHILE NECESSARY
** =======================================
*/

int	ft_move(char **buf)
{
	int	i;

	i = 0;
	while (*(*buf - 5) != '#' && *(*buf + 5) != '#' && **buf != '#'
		&& *(*buf + 1) != '\0' && **buf != '\n' && **buf)
		++(*buf);
	if (*(*buf + 5) == '#' && **buf != '\n' && *(*buf + 1) != '\n' && **buf)
	{
		while ((*buf)[i] != '#' && (*buf)[i] != '\n')
			++i;
		if ((*buf)[i] == '\n')
		{
			while (*(*buf - 1) != '\n')
				++(*buf);
			return (ft_move(&(*buf)));
		}
	}
	if (!(**buf))
		--(*buf);
	return (0);
}
